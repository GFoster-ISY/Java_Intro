package P02_Control;

public class C06_Stopping_P1 {
    public static void main(String[] args) {
        String message = "A long time ago in a galaxy far, far away....";
        System.out.println(message.substring(0,15));
        System.out.println(message.substring(16));
        System.out.println(message.substring(19,20));
        System.out.println(message.substring(21,27));
        System.out.println(message.substring(28,41));
        System.out.println(message.substring(41));
    } // end of main method
}
